  def self.collect_translated
   COLLECTION.keep_if {|key,value| key != value }
  end

  def build_methods
    Object.constants.reduce([]) {|memo, klass|
      klass = Object.const_get klass
      memo << klass.methods
      memo << klass.instance_methods if klass.respond_to? :instance_methods
      memo
    }.flatten.compact.uniq.sort.map(&:to_s).map{|k| [k, k]}.to_h  
  end


